//write a program the numbers from 10 to 50 using for loop/while loop.
//output=Negative
public class Negativenumber {

    public static void main(String[] args) {
        System.out.println("The negative numbers from -10 to -50 are:");
        for (int i = -10; i >=- 50; i--) {
            System.out.println(i);
        }
    }
}